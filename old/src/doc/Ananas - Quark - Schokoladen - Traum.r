500 g 	Quark
1 Becher 	Sahne
3 EL 	Zucker
4 Scheibe/n 	Ananas
10 	Löffelbiskuits
1 Tafel 	Schokolade, zartbitter

Den Quark mit dem Zucker gut verrühren. Die Sahne steif schlagen und vorsichtig unter den Quark ziehen. Die 4 Ananasscheiben sowie die Löffelbiskuits in kleine Stücke schneiden. Die Schokolade mit einem großen Messer klein hacken (die Arbeit lohnt sich, geht schnell und schmeckt viel besser als fertige Schokoraspel).

In 4 größere Dessertschalen erst 1 - 2 Esslöffel Quarkmasse geben. Darauf die Löffelbiskuits verteilen. Wieder 1 - 2 Esslöffel Quarkmasse darauf geben. Dann die Ananas darüber streuen. Den Rest der Quarkmasse auf den Ananasstücken verstreichen und großzügig die Schokoladenraspel darüber streuen.

Die Schichtspeise bis zum Servieren gut abgedeckt in den Kühlschrank stellen. Das Dessert ist schnell gemacht und sieht super aus. Wer keine Ananas mag, kann gerne auch anderes Obst wie Pfirsiche oder Beeren nehmen. 
