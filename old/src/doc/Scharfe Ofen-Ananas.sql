1 	Ananas
2 EL 	Olivenöl
	Salz
	Chiliflocken 

Die ungeschälte Ananas mit Grün längs halbieren, die Hälften jeweils längs vierteln und das Fruchtfleisch mit Olivenöl einreiben. Mit Salz würzen und auf das Blech im Backofen legen. 20 Minuten bei 200 °C backen.

Die Ananas-Spalten auf einer Platte anrichten und mit Chiliflocken bestreuen.

Hinweis: Der Strunk der Ananas wird durch das Schmoren im heißen Ofen weich und kann problemlos mitverzehrt werden.

