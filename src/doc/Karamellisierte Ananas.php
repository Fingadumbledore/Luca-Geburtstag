1 	Ananas
3 EL 	Zucker
1 	Vanilleschote(n)
50 ml 	Rum, (optional)
etwas 	Wasser 

Den Zucker in einer Pfanne auf mittlerer Hitze schmelzen und karamellisieren. Die Ananas schälen, den Strunk entfernen und in Stücke schneiden. Dann in den karamellisierten Zucker legen. Die Vanilleschote auskratzen, das Mark und die Schote in die Pfanne geben. Einen Schuss Rum dazugeben und verdampfen lassen. Etwas Wasser dazugeben und ca. 5 Minuten schmoren. Vor dem Servieren die Vanilleschote entnehmen. 
